# Andi + Budi = 49 Tahun
# Rasio Usia = 0.4 Rasio 4 / 10 => 4 : 10 => 2 : 5 (andi) (budi)
# Umur 2 tahun lagii 

totalUmur = 49
ratioAndi = 2
ratioBudi = 5
ratioUmur = 7 

usiaAndi = totalUmur * (ratioAndi/ratioUmur)
usiaBudi = totalUmur * (ratioBudi/ratioUmur)

print('Usia Andi Sekarang: {}' .format(usiaAndi))
print('Usia Budi Sekarang: {}' .format(int(usiaBudi)))

print('Usia Andi 2 Tahun Lagi: {}' .format((int(usiaAndi) + 2)))
print('Usia Budi 2 Tahun Lagi: {}' .format((int(usiaBudi) + 2)))